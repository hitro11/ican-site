  <div class="res-mini-sec">
    <h5 class="col-pri">Will I have to take a medical exam?</h5>
    <p>
      All prospective immigrants to Canada are required to undergo medical examinations. These 
      examinations are intended to detect any conditions which may affect the health of the Canadian
      public, or which may result in excessive demands being placed upon the Canadian health care 
      system.
    </p>
    <p>
      The medical examination includes a standard physical examination, blood tests, urine tests, 
      and X-Rays.
    </p>
