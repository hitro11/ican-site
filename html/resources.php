  <div class="res-mini-sec">
    <h5 class="col-pri">What is a Canadian Immigrant Visa?</h5>
    <p>
      An Immigrant Visa is a document which allows a person to live and work anywhere in Canada,
      and confers upon that person permanent resident status. It comes with certain responsibilities
      and can be revoked if the holder is out of the country for too long, or is guilty of some 
      criminal activity. A person who is a Canadian permanent resident may apply for Canadian 
      Citizenship after 3 years.
    </p>
  </div>

  <div class="res-mini-sec">
    <h5 class="col-pri">How is an Employment Authorization different from an Immigrant Visa?</h5>
    <p>
      An Employment Authorization permits an eligible visitor to reside and work in Canada for a limited
      period of time, and restrictions are usually placed on the type of employment which can be 
      pursued. It will not, by itself, lead to Canadian permanent resident status. By contrast, a
      Canadian Immigrant Visa entitles its holder to live and work anywhere in Canada, enjoy
      many of the privileges of Canadian Citizenship, apply for Canadian Citizenship after 3
      years and sponsor family members for Canadian permanent resident status.
    </p>
  </div>

  <div class="res-mini-sec">
    <h5 class="col-pri">Is there a benefit to using a representative for immigration to Canada?</h5>
    <p>
      Statistically, your chances of succeeding are increased if you are represented by a 
      qualified representative. But expert representation is not demanded by the 
      Canadian immigration authorities. 
    </p>
  </div>

  <div class="res-mini-sec">
    <h5 class="col-pri">Do I become a permanent resident of Canada as soon as my landing documents are issued?</h5>
    <p>
      No. You will only become a permanent resident when you cross a Canadian port-of-entry with your
      valid passport and your valid Canadian Immigrant Visa.
    </p>
  </div>

  <div class="res-mini-sec">
    <h5 class="col-pri">Will I be interviewed by an immigration officer?</h5>
    <p>
      Most Skilled Worker applicants for permanent resident status are required to attend a selection
      interview with an immigration officer. Such interviews are held to ensure the information in the
      application is accurate, to verify documentation, to test language ability, and to help 
      applicants relocate to Canada.
    </p>
    <p>
      Visa offices may grant certain Skilled Worker candidates an interview waiver, depending on the
      qualifications of the applicant, the quality of the supporting documentation, and the overall
      credibility of the applicant. The likelihood of an interview waiver varies from one visa office
      to another.
    </p>
    <p>
      Almost all applicants under the Business Immigration Program will be required to attend a 
      selection interview.
    </p>
  </div>

  <div class="res-mini-sec">
    <h5 class="col-pri">Is there anything I can do to obtain an interview waiver?</h5>
    <p>
      Applications which are complete in every detail increase the chances of an interview waiver. 
      However, interview waivers are granted at the discretion of the immigration officials. It is 
      not possible to apply specifically for a waiver. Even if an interview is waived, you may 
      still be called to an interview, generally for quality assurance purposes.
    </p>
  </div>

